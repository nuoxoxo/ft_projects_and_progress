#include "minishell.h"

int	g_exit_status = ON_SUCCESS;

int	main(int c, char **v, char **env)
{
	if (c ^ 1)
		usage();
	
	/* main loop */
	catch_signal();
	start_shell(env);

	/* end */
	(void) v;
	return (g_exit_status);
}
