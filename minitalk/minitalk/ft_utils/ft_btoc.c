#include "../minitalk.h"

char	ft_btoc(char *bs)
{
	char	c;
	int		pow;
	int		i;

	i = (int) ft_strlen(bs);
	pow = 1;
	c = 0;
	while (--i > -1)
	{
		c += pow * (bs[i] - '0');
		pow *= 2;
	}
	return (c);
}
