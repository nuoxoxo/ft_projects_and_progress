#include "../minitalk.h"

char	*ft_strcat(char *des, const char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (des[i])
	{
		i++;
	}
	while (src[j])
	{
		des[i + j] = src[j];
		j++;
	}
	des[i + j] = 0;
	return (des);
}
